const horde = {
    aliens: [[],[],[],[],[]],
    movementdirection: 0,
    location: 0,
    populate: function(){
        for(let i = 0; i < 13; i++) {
            this.aliens[0].push(new alien(3));
            this.aliens[1].push(new alien(2));
            this.aliens[2].push(new alien(2));
            this.aliens[3].push(new alien(1));
            this.aliens[4].push(new alien(1));
        }
    },
    draw: function(){
        let posx = this.location;
        let posy = 50;
        this.aliens.forEach((row)=>{
            row.forEach((alien) => {
                renderShapeFromLogic(alien.logic[animationState], posx, posy, 3, alien.color);
                posx += 42;
            })
            posx = this.location;
            posy += 42;
        })
    },
    move: function(){
        animationState = animationState ? 0 : 1;

        if(this.location >= 500){this.movementdirection = 0}
        if(this.location <= 0)(this.movementdirection = 1)
        if(this.movementdirection){this.location += 6}
        else{this.location -= 6};
    }
}

horde.populate();